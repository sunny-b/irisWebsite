				<h3>Rope Type Foundry</h3>
				<h4>Exhibition / Graphic / Web / Type Design</h4>
				<p>Rope is a fictional type foundry created by <a href="http://www.shiva-n.com/" target="_blank">Shiva Nallaperumal</a> and Iris Sprague as part of their <a target="_blank" href="http://thesis2015.micadesign.org/ropetype/">thesis project</a> (2015) at the GD MFA program at the Maryland Institute College of Art (MICA).</p>
				<p>Iris concentrates on designing Theo, a large super family exploring different styles in the history of type with one skeleton. Theo is a super family containing the complementary sub families Theo Humanist & Theo Grotesque (Sans), Theo Text (Serif) and Theo Italian (Display) each with a range of weights and matching italics/obliques.</p>
				<p>Shiva explores a range of typographic problems and solutions through the design of several different type families for specific and non specific uses. Among them are a quasi artificially intelligent display face, a reverse stress sans for text, a humanist sans and slab with contradictory strokes, a caption face for extremely small sizes and a ridiculous informal sans.</p>
				<p>Throughout the year, Rope Type Foundry created 8 typefaces, an exhibition, a physical and digital specimen, book and website. Iris and Shiva worked independently to create their typefaces, but their exhibition was a collaborative effort. Shiva took ownership of the physical aspects, such as the specimen and book. Iris was in charge of the digital relm, which included the animation and <a href="ropetype.php">website</a>.</p>
				
