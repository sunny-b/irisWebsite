				<h3>RopeType.com</h3>
				<h4>UI/UX &amp; Type Design</h4>
				
				<p>RopeType.com is a fictitious website that <a href="http://www.shiva-n.com/" target="_blank">Shiva</a> and I created to store and present the typefaces for our <a href="rope-type-foundry.php">thesis</a>. The URL ropetype.com does not exist, but if you wish to see the site you may do so <a target="_blank" href="http://thesis2015.micadesign.org/ropetype/">here</a>.</p>
				<p>It was important for us to curate the experience for the viewer because we came into our thesis as graphic designers first and type designers second. This can especially be seen in our <a href="http://thesis2015.micadesign.org/ropetype/Typefaces.html" target="_blank">type tester page</a>. All typefaces are used as headline, body, and/or caption text. I wanted to display the typefaces in a way to always show how they would used in the real world.</p>
				<p>Another important aspect of the website is that is houses our <a href="http://ropetype.tumblr.com" target="_blank">process page</a>. Shiva and I had done many type sprints to discover the character of Rope Type Foundry. We would assign each other parameters and challenge each other to create content. This resulted in the majority of the posters we used in our exhibition.</p>
				
